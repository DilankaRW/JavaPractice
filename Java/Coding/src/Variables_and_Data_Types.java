public class Variables_and_Data_Types {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a);

        double b = 10.25;
        System.out.println(b);

        char c = 'C';
        System.out.println(c);

        boolean d = true;
        System.out.println(d);

        String e = "Hello!";
        System.out.println(e);
    }
}