// Logical Operators
// AND - &&
// OR - ||

package PracticeFile3;

public class P20 {
    public static void main(String[] args) {
        int x = 5;
        int y = 10;

        // AND Operator - &&

        if (x>7 && y>7){
            System.out.println("OK");
        }else {
            System.out.println("NO");
        }
//        Output - NO

        // OR Operator - ||

        if (x>7 || y>7){
            System.out.println("OK");
        }else {
            System.out.println("NO");
        }
//        Output - OK
    }
}
