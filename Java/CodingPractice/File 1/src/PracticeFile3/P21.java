// Variables and Data Types

package PracticeFile3;

public class P21 {
    public static void main(String[] args) {
        int a = 5;
        System.out.println(a);

        double b = 5.15;
        System.out.println(b);

        char c = 'C';
        System.out.println(c);

        boolean d = true;
        System.out.println(d);

        String e = "Hello!";
        System.out.println(e);
    }
}
